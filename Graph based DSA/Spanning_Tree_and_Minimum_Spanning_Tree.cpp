						/** Spanning Tree **/
/*
	- An undirected graph is a graph in which the edges do not point in any 
	direction.
	
	- A connected graph is a graph in which there is always a path from a
	vertex to any other vertex.
	
	- A spanning tree is a sub-graph of an undirected connected graph, which
	includes all the vertices of the graph with a minimum possible number of
	edges. If a vertex is missed, then it is not a spanning tree.
	
	- The edges may or may not have weights assigned to them.
	
	- The total number of spanning trees with n vertices that can be created 
	from a complete graph is equal to n^(n-2).
	
	- The minimum spanning tree from a graph is found using the following algorithm:
		1. Prim's Algorithm
		2. Kruskal's Algorithm
	
	- Spanning Tree Applications:
		1. Computer Network Routing Protocol
		2. Clustrer Analysis
		3. Civil Network Planning
		
	- Minimum Spanning tree Applications:
		1. To find paths in the map
		2. To design networks like telecommuiction networks, water supply networks,
		and electrial grids.
*/
